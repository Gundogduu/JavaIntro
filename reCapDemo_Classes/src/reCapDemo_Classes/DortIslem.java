//Classlarýn en önemli özelliði ortak operasyonlarý tutumak, bir diðer özelliðide özellik(field veya attribute olarak isimlendirilirler) tutmaktýk.
public class DortIslem {
	public int Topla(int sayi1,int sayi2) {
		return sayi1+sayi2;
	}
	
	public int Cikar(int sayi1,int sayi2) {
		return sayi1-sayi2;
	}
	
	public int Carp(int sayi1,int sayi2) {
		return sayi1*sayi2;
	}
	
	public int Bol(int sayi1,int sayi2) {
		return sayi1/sayi2;
	}
}
